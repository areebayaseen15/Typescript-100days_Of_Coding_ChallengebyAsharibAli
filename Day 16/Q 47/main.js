// Qno 47:
//  Array Destructuring: Given an array of objects representing different laptops, each
// with properties make, model, and year, use array destructuring to assign the first and second
// laptops to variables. Then, log these variables.
var laptops = [
    {
        make: "hp",
        model: "ProBook 6470b",
        Year: "2020",
    },
    {
        make: "Dell",
        model: "XPS 15",
        Year: "2022",
    },
    {
        make: "apple",
        model: "Macbook pro",
        Year: "2022",
    },
];
var laptop1, laptop2 = laptops;
console.log(laptop1);
console.log(laptop2);
